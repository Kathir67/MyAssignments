package week1.day2;

public class PositiveOrNegative {

	public static void main(String[] args) {
		int input = 0;
		
		if (input >= 1) {
			System.out.println("Given is positive");
		} else if (input <= -1) {
			System.out.println("Given is negative");
		} else if (input == 0) {
			System.out.println("Given is neither positive not negative");
		}

	}

}
